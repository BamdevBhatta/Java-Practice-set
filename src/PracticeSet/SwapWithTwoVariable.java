package PracticeSet;

public class SwapWithTwoVariable {
public static void main(String[] args) {
	int a = 30;
	int b = 40;
	System.out.println("Number Bofore Sorting a="+a+" and b= "+b);
	a=a+b;//a=30 (10+20)    
	b=a-b;//b=10 (30-20)    
	a=a-b;//a=20 (30-10)
	System.out.println("Number After sorting are a= "+a+" and b= "+b);
}
}
